## O que é FizzBuzz?
 
 O objetivo do FizzBuzz é “testar” se uma pessoa sabe codificar. É comumente usado em entrevistas para testar as habilidades de resolução de problemas e codificação de um candidato.

 O que é tão interessante sobre o FizzBuzz é que sua solução é contra-intuitiva e não pode ser resolvida de forma elegante ou eficiente, o que o torna um desafio interessante.
 
  Suas instruções são:
 
- `Escreva um programa que imprima os números de 1 a 100`
- `Para números divisíveis por 3, imprima "Fizz"`
- `Para números divisíveis por 5, imprima “Buzz”`
- `Para números divisíveis por 3 e 5, imprima “FizzBuzz”`
 
